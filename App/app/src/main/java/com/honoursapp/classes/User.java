package com.honoursapp.classes;

public class User {

    String name;
    String email;

    // Default Constructor
    public User(){

    }
    
    // Getters and setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

}
